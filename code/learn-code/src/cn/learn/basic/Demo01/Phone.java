package cn.learn.basic.Demo01;

/*
   定义类：
       使用类的形式，对现实中的事务进行描述
	   事物：方法，属性
	   方法：这个事物具备的功能
	   属性：变量
	   
	  格式：
	    public class 类名{
			属性定义
			  修饰符 数据类型 变量名 = 值
			  
			方法定义
			  修饰符 返回值类型 方法名(参数列表){
				  
			  }
		}
		
	  要求：使用Java中的类，描述生活中的手机这个事物
	    手机事物，具备哪些属性，属于他自己的特性
		    颜色 品牌 大小
*/
public class Phone{
	/*
	  定义手机的属性
	*/
	String color;
	String brand;
	double size ;
}