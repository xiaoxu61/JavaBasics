package cn.learn.basic.Demo24;

public class Demo24Person {
    private String name;
    private int age;

    public Demo24Person() {
    }

    public Demo24Person(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
